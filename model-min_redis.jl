    #sum(vc[i] * (r_up[t, i] + r_down[t, i]) for i in I) ==
    #sum(beta_pos_up[t, i] * (q_max[t, i] - q[t, i]) for i in I) +
    #sum(beta_neg_up[t, i] * q[t, i] for i in I) +
    #sum(rho_up[t, l] * Fmax[l] + rho_low[t, l] * Fmax[l] for l in L)
 # strong duality for redispatch at time t

# Demand block


#@variable(m, mu_D_low[t in T] >= 0) # dual variable for D[t] >= 0, incl dual constraint
#@variable(m, mu_D_up[t in T] >= 0) # dual variable for D[t] <= D_max[t], incl dual constraint

#@expression(m, D[t in T], sum(d[t in T, n in N] for n in N)) # aggregate load at time t
