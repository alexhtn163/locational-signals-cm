# objective: system-level redispatch objective only
@objective(m, Min, sum(vc[i] * (r_up[t, i] + r_down[t, i]) for t in T, i in I)) # minimize redispatch cost
# social welfare maximization objective
#@objective(m, Max, sum(lambda_e[t] * D_max[t] - sum(vc[i] * q[t, i] for i in I) for t in T))

set_optimizer_attribute(m, "TimeLimit", 700)   
set_optimizer_attribute(m, "NonConvex", 2)
set_optimizer_attribute(m, "FeasibilityTol", 1e-5)
set_optimizer_attribute(m, "DualReductions", 0)
set_optimizer_attribute(m, "IISMethod", 1)  # Optional, chooses IIS algorithm
optimize!(m)

const MOI = MathOptInterface

println("Termination status: ", termination_status(m))

if termination_status(m) == MOI.OPTIMAL
    println("Redispatch objective: ", objective_value(m))
    println("Capacity requirement (max residual load): ", d_c)

    for i in I
        kept_capacity = value(disinvest[i])
        retired_capacity = cap[i] - kept_capacity
        println(i,
            " initial cap = ", cap[i],
            " kept cap = ", kept_capacity,
            " retired cap = ", retired_capacity,
            " q = ", max(value(q[t, i]) for t in T),
            " r_up = ", max(value(r_up[t, i]) for t in T),
            " r_down = ", max(value(r_down[t, i]) for t in T))
    end

    for l in L
        println("Line ", l,
            " flow = ", max(value(p_flow[t, l]) for t in T),
            " / limit = ", Fmax[l])
    end
    for t in T
        println("Price at time ", t, ": ", value(lambda_e[t]))
        println("Capacity price: ", value(lambda_c))
        
    end
    for t in T 
        println("Total up redispatch at time ", t, ": ", sum(value.(r_up[t, i] for i in I )))
    end
    for t in T 
        println("Total downwards redispatch at time ", t, ": ", sum(value.(r_down[t, i] for i in I )))
    end

    if sum(value.(r_up[t, i]) - value.(r_down[t, i]) for t in T, i in I) == 0
        println("Net redispatch is zero at optimality, as expected.")
    else
        println("Warning: Net redispatch is not zero at optimality, which may indicate an issue with the model.")
    end

    # installed and retired capacities
    total_initial_capacity = sum(cap[i] for i in I; init = 0.0)
    total_kept_capacity = sum(value(disinvest[i]) for i in I; init = 0.0)
    total_retired_capacity = sum(cap[i] - value(disinvest[i]) for i in I; init = 0.0)
    println("Total initial capacity: ", total_initial_capacity)
    println("Total kept capacity: ", total_kept_capacity)
    println("Total retired capacity: ", total_retired_capacity)

    tech_labels = Dict(
        "CCGT" => "CCGT",
        "biomass" => "Biomass",
        "onwind" => "Onshore Wind",
        "offwind-ac" => "Offshore Wind",
        "waste" => "Waste",
        "nuclear" => "Nuclear"
    )

    for n in N
        node_kept_capacity = sum(value(disinvest[i]) for i in I if gen_bus_idx[i] == n; init = 0.0)
        println("Node ", n, " kept capacity: ", node_kept_capacity)

        for t_name in keys(tech_labels)
            if any(i -> gen_bus_idx[i] == n && tech[i] == t_name, I)
                node_tech_kept_cap = sum(value(disinvest[i]) for i in I if gen_bus_idx[i] == n && tech[i] == t_name; init = 0.0)
                println("  ", tech_labels[t_name], ": ", node_tech_kept_cap)
            end
        end
    end

end

if termination_status(m) == MOI.INFEASIBLE
    set_optimizer_attribute(m, "TimeLimit", 700)  
    compute_conflict!(m) 
    println("Conflict status: ", get_attribute(m, MOI.ConflictStatus()))
end

for (func_type, set_type) in list_of_constraint_types(m)
    for c in all_constraints(m, func_type, set_type)
        if MOI.get(m, MOI.ConstraintConflictStatus(), c) == MOI.IN_CONFLICT
            println("Infeasible constraint: ", c)
        end
    end
end
for v in all_variables(m)
    if MOI.get(m, MOI.VariableConflictStatus(), v) == MOI.IN_CONFLICT
        println("Infeasible variable: ", v)
    end
end